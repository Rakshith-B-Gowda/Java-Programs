package classtest;

public class Books {
	
	private String name;
	private int units;

	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setUnits(int units) {
		this.units = units;
	}
	
	public int getUnits() {
		return units;
	}
}
